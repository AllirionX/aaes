<h2>Présentation</h2>
<ul>
<li>Au cours d'un ZOMBIE SURVIVAL, les participants sont confrontés à une invasion zombie. </li>
<li>Livrés à eux-même en pleine nuit, ils doivent se débrouiller pour survivre en effectuant de nombreuses missions et peut-être découvrir l'origine de <span class="red">l'invasion</span>. Mais attention, chaque participant attrapé par un zombie change de camp pour traquer ses anciens amis... </li><li>Angoissante pour les humains, exultante pour les zombies, soyez prêts à tout.</li>
</ul>

<h2>Déroulement</h2>
<ul>
<li>Arrivez 15 minutes avant l'heure de début prévue.</li>
<li>Dès que la soirée est commencée, si un humain se fait attraper par un zombie, il est emmené au camp des zombies pour être <span class="red">maquillé</span> et devenir à son tour un zombie.</li>
<li>Des <span class="red">missions</span> sont organisées tout au long de la soirée pour aider les humains à survivre.</li>
</ul>

<h2>Humains</h2>
<ul>
<li>Les humains sont <span class="red">libres</span> (dans le cadre des règles).</li>
<li>Les humains sont identifiés par les <span class="red">bracelets lumineux</span> qu'ils doivent porter de manière visible.</li>
<li>Les humains ne peuvent pas tuer les zombies.</li>
<li>Les humains doivent réussir les missions proposées pour assurer leur <span class="red">survie</span>.</li>
<li>Si tenter de survivre s'avère trop stressant, les humains peuvent se laisser attraper.</li>
</ul>

<h2>Zombies</h2>
<ul>
<li>Le zombie est le stade <span class="red">supérieur</span> de l'évolution humaine.</li>
<li>Les zombies peuvent ouvrir les portes, grimper etc.</li>
<li>Les zombies <span class="red">marchent</span> et ne courent pas.</li>
<li>Les zombies n'utilisent pas de lampe.</li>
<li>Les camps protégés, signalés par des marquages fluorescents, sont interdits aux zombies.</li>
</ul>

<h2>Matériel</h2>
<ul>
<li>Les participants amènent leur propre matériel : gants, vêtements de pluie, vêtements salissables, chaussures de sport, eau, sac à dos, etc.</li>
<li>Les lampes à led pouvant éblouir les participants sont interdites. Seules les <span class="red">lampes torches</span> à filament non éblouissantes sont autorisées.</li>
</ul>

<h2>Règles importantes</h2>
<ul>
<li>L'<span class="red">équipe d'organisation</span> ne souhaite qu'une chose : vous faire passer une soirée réussie. Elle a toujours raison, et il est primordial de la respecter. Elle peut changer les règles de la soirée à tout moment.</li>
<li>Pour que chacun s'amuse, n'oubliez pas de rester <span class="red">fair play</span> et de bonne humeur !</li>
<li>Bien évidemment, armes, alcool et substances illicites sont interdites.</li>
</ul>
